package com.kita.first.level1;

public class If {
	public static void main(String [] args)
	{
		int n1 = 4;
		int n2 = 3;
		
		if(n1 > n2)
		{
			System.out.println("n1이 n2보다 크다.");
		}
		else if(n1 == n2)
		{
			System.out.println("n1 과 n2가 같습니다.");
		}
		else
		{
			System.out.println("n1이 n2보다 작다.");
		}
	}

}
